### Examples of Tokens:

# Identifiers
variable_name = "Hello"  # 'variable_name' is an identifier

def my_function():  # 'my_function' is an identifier
    return "Python Tokens"

# Keywords
for i in range(5):  # 'for' and 'in' are keywords
    print(i)

# Operators
a = 10 + 5  # '+' is an operator
b = a * 2   # '*' is an operator

# Delimiters
my_list = [1, 2, 3, 4]  # Square brackets '[]' are delimiters
my_dict = {"key": "value"}  # Curly braces '{}' are delimiters

# Literals
string_literal = "Hello, World!"  # String literal
integer_literal = 42  # Integer literal
float_literal = 3.14  # Float literal
boolean_literal = True  # Boolean literal
